                            STREETBALL KOMPO

                                 Co?
                     Turniej koszykówki ulicznej

                                Kiedy?
                   W pierwszy dzieï party (piâtek)

                                 Kto?
        Kategoria  open.  Graê moûe kaûdy kto ma identyfikator z party
i  zgîosi  druûynë  do  turnieju...   Mogâ  byê chîopcy i dziewczynki,
starsi   i  mîodsi.   Chodzi  gîównie  o  zabawë.   Nie  sâ  konieczne
zdolnoôci.  Sâ konieczne chëci, wola walki i rywalizacji!

                                  Jak?
        Sposób  rozegrania compotu zaleûy od tego, ile druûyn weúmie w
nim  udziaî, jednak warianty sa generalnie trzy:  pucharowo (przegrany
odpada)  - tu potrzebna jest wiëksza iloôê zespoîów; kaûdy z kaûdym...
-  to  wariant  dla  mniejszej  iloôci  druzyn;  grupowo  a  nastëpnie
pucharowo  -  czyli  najlepsi  awansujâ  a  nastepnie  grajâ  systemem
pucharowym - do tego wariantu rownieû potrzebnych jest wiele druûyn...

        Mecze bëdâ trwaîy do zdobycia 15pkt.  z przewagâ minimum 2pkt.
lub  20min.   -  zaleûnie  od  tego,  co  pierwsze nastâpi.  Punkty sâ
liczone  nastëpujaco:   1pkt.   -  do  wyznaczonej linii, 2pkt.  - zza
wyznaczonej  linii,  rzuty osobiste (2x1pkt.) sâ tylko w wypadku faulu
technicznego.   Piîka po kaûdym faulu i zdobyciu punktów jest wybijana
zza wyznaczonej linii.

                                 Zapisy
        Naleûy  zapisywaê sië na party lub przed party u Deekay'a.  Na
party  zapisy  bëdâ  przyjmowane  na  póî  godziny  przed rozpoczëciem
compotu...   Przed party moûna bëdzie zapisywaê sië u Deekay'a.  Snail
czy  email  - nie ma róûnicy.  Moûna zapisywaê caîe druûyny, ale moûna
równieû   zapisywaê   sië   indywidualnie   -  tak  aby  wyraziê  chëê
uczestniczenia  w  kompotach.   Na party stworzylibyômy druûynë z tych
pojedyïczych  osób  lub  teû  przydzielilibyômy  ich do druûyny, gdzie
brakuje zawodników.


                             Jakie druûyny?
        Druzyny   3-osobowe.    Mogâ  byê,  ale  nie  muszâ  zawodnicy
rezerwowi.   Zmiany  nastepujâ  w czasie gry, nie potrzebna jest na to
zgoda sëdziego.

        Co  do  nagród,  to wszystko zaleûy od budûetu organizatorów i
tym  samym iloôci scenowców przybyîych na party.  Tak wiëc namawiajcie
swoich kumpli na przyjazd, szykujcie skîady oraz kondycjë i obyômy sië
wszyscy dobrze bawili.

