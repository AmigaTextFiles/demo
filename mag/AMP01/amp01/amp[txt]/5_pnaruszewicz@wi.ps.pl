         - Pomoc od kodera -

   Po prawie dwóch  latach  od  numeru
zerowego, znów nadszedî czas na  kilka
sîów  od  kodera.  Jak  widzicie   kod
magazynu przeszedî  pewnâ  metamorfozë
i z przeglâdarki pod Workbench'a  mamy
teraz  otwierajâcy  sië   na   osobnym
ekranie magazyn z  grafikâ  i  muzykâ.
Oczywiôcie   i   tym   razem    mojemu
"dziecku" brakuje nieco do ideaîu, ale
chyba moûecie byê zadowoleni.

   Zanim jednak zajmiecie sië lekturâ,
polecam przyjrzeê sië ikonce  "AMP#1",
a  w  szczególnoôci  jej  tooltype'om.
Znajdziecie tam trzy pozycje:

CONFIG - ustawiony na ON powoduje,  ûe
po   uruchomieniu    maga    bëdziecie
poproszeni  o  wybranie   ekranu,   na
którym  zostanie  uruchomiony  magazyn
oraz  intro.  Taki  sam  efekt   moûna
osiâgnâê   przez   skasowanie    pliku
"amp1.cfg". Skasowanie tooltype'u albo

åtooltypes

ustawienie  go  na  OFF  nie  powoduje
ûadnych konsekwencji.

MUSIC - ustawiony na OFF powoduje,  ûe
magazyn   zostanie   uruchomiony   bez
muzyki [muzyka nie bëdzie wczytana  do
pamiëci],    dziëki    czemu    moûemy
zaoszczëdziê troszkë ram'u [ok.400kb].
Wadâ  tego  rozwiâzania  bëdzie   brak
moûliwoôci ponownego wîâczenia  muzyki
w trakcie pracy  magazynu.  Skasowanie
tooltype'u albo ustawienie go  na  OFF
powoduje  normalne  uruchomienie  maga
z muzykâ.

ANIM - ustawiony na OFF  powoduje,  ûe
magazyn   zostanie   uruchomiony   bez
animacji [animacje nie bëdâ wczytywane
do  pamiëci],  dziëki   czemu   moûemy
zaoszczëdziê troszkë ram'u [ok.100kb].
Wadâ  tego  rozwiâzania  bëdzie   brak
moûliwoôci     ponownego     wîâczenia
animacji  w  trakcie  pracy  magazynu.
Skasowanie tooltype'u albo  ustawienie
go   na    OFF    powoduje    normalne
uruchomienie maga z animacjami.

   Po omówieniu tooltype'ów przejdë do
wîaôciwej instrukcji obsîugi magazynu.
Magazyn      obsîugujemy      tandemem
klawiatura+myszka    w     nastëpujâcy
sposób:


Przyciski [panel dolny]:

"Strzaîka w prawo" - nastëpna strona
"Strzaîka w lewo"  - poprzednia strona
"Podwójna strzaîka w lewo"
                   - poprzedni artykuî
"Podwójna strzaîka w prawo"
                   - nastëpny artykuî
"Piîka"            - powrót do menu


Przyciski [panel górny]:

"Czerwona kartka" - wyjôcie z magazynu
"Nutka"           - wyî/wî muzyki
"Kamera"          - wyî/wî animacji

Klawisze:

"Strzaîka w dóî"  - nastëpny artykuî
"Strzaîka w górë" - poprzedni artykuî
"Strzaîka w lewo" - poprzednia strona
"Strzaîka w prawo"- nastëpna strona
"Spacja"          - powrót do menu
"Esc"             - wyjôcie z magazynu

